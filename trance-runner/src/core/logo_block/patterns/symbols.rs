pub fn pattern(ch: char) -> Option<[&'static str; 5]> {
    match ch {
        '_' => Some([
            "     ",
            "     ",
            "     ",
            "     ",
            "\u{2588}\u{2588}\u{2588}\u{2588}\u{2588}",
        ]),
        '!' => Some([
            "  \u{2588}  ",
            "  \u{2588}  ",
            "  \u{2588}  ",
            "     ",
            "  \u{2588}  ",
        ]),
        ' ' => Some(["     ", "     ", "     ", "     ", "     "]),
        '.' => Some(["     ", "     ", "     ", "     ", "  \u{2588}  "]),
        '-' => Some([
            "     ",
            "     ",
            " \u{2588}\u{2588}\u{2588} ",
            "     ",
            "     ",
        ]),
        _ => Some([
            " \u{2588}\u{2588}\u{2588} ",
            "\u{2588}   \u{2588}",
            "\u{2588}   \u{2588}",
            "\u{2588}   \u{2588}",
            " \u{2588}\u{2588}\u{2588} ",
        ]),
    }
}
